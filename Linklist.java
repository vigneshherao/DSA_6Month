import java.util.HashSet;

public class Linklist{
    public static void main(String[] args) {
        HashSet<Integer> link = new HashSet<Integer>();
        link.add(2);
        link.add(5);
        link.add(7);
        link.add(90);
        for (Integer i : link) {
            System.out.println(i);
        }


        

    }
}